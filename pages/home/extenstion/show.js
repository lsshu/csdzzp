  /**
   * 生命周期函数--监听页面加载
   */
  onLoad: function (options) {
    let extenstion_id = options.extenstion_id;
    req.post('/getExtension/' + extenstion_id).then(({ data }) => {
      var extenstion = data.data;
      extenstion.content = extenstion.content.replace(/\<img(.*?)style/gi, '<img$1styleb ');
      extenstion.content = extenstion.content.replace(/\<img/gi, '<img style="width:100%;height:auto" ');
      this.setData({ extenstion: extenstion });
    });
  },

